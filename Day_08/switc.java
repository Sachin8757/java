public class switc {
    public static void main(String args[]){
        int a=5;
        switch (a) {
            case 1:
            System.out.println("1");
                
                break;
                case 2:
                System.out.println("2");
                break;
                case 5:
                System.out.println("5");
                break;
        
            default:
            System.out.println("Nothing");
                break;
        }
    }
}
