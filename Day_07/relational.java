public class relational {
    public static void main(String args[]){
        //Relational Operators
        // == , != ,> ,< , >= , <= are relational operators
        int a=5;
        int b=6;
        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a>=b);
        System.out.println(a<=b);
    }
}
